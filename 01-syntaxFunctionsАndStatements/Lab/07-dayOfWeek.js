function dayOfWeek(params) {

    if (params == 'Monday') {
        console.log(1);
    } else if (params == 'Tuesday') {
        console.log(2);
    } else if (params == 'Wednesday') {
        console.log(3);
    } else if (params == 'Thursday') {
        console.log(4);
    } else if (params == 'Friday') {
        console.log(5);
    } else if (params == 'Saturday') {
        console.log(6);
    } else if (params == 'Sunday') {
        console.log(7);
    } else {
        console.log('error');
    }

}
dayOfWeek('Friday')
